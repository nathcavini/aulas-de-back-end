<h2>Variáveis</h2>
<p>
    São estruturas (memória) utilizada
    para armazenar dados que serão processados
    pelo progama.
</p>

<p>
    Para criar uma variável em PHP basta colocar
    símbulo de $ na frente de um nome e atribuir
    um valor com o símbulo de igual. Exemplo:
    <pre>
    $variavel = 10;
    $nome = "Maria";
    $nota = 5.5;
    </pre>
</p>

<?php
    $nome = "Nathália";
    echo "Olá $nome, seja bem-vinda à aula de backend.";
?> 